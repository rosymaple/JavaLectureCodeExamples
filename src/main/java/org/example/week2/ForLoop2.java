package org.example.week2;

public class ForLoop2 {
    public static void main(String[] args) {        // end of main method

        System.out.println("Here are all the dates in January");        // print message for user
        for (int day = 1 ; day <= 31 ; day++) {     // count from 1 to 31 in this for loop...
            System.out.println("January " + day);       // print each day of January from 1 to 31...
        }       // end of for loop
    }       // end of main method
}       // end of public class
