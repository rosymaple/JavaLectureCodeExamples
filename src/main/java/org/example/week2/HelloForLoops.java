package org.example.week2;

public class HelloForLoops {
    public static void main(String[] args) {    // start of main method
        for (int x = 0; x < 10; x = x++) {    //for loop declaring x as 0, if x is less than 10, loop over x infinitely
            System.out.println(x);      // prints the value of x infinitely
        }       // end of for loop counter over x
    }       // end of main method
}       // end of public class. this code prints infinite zeros. don't remember what this program was for exactly
