package org.example.week4;

public class ArrayLoop {
    public static void main(String[] args)  {       // start of main method

        String[] days = {"Mon", "Tue", "Wed", "Thu", "Fri"};       // initialize array variable "days"
        for (String day: days)  {       // loop through "days" array
            System.out.println(day);        // print each day in days array
        }       // end of for loop over days array
    }       // end of main method
}       // end of public class
